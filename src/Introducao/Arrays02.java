package Introducao;

public class Arrays02 {
    public static void main(String[] args) {

        String[] nomes = new String [3];
        nomes[0] = "Vinicius";
        nomes[1] = "Gabriel";
        nomes[2] = "Fernando";

        for (int i = 0; i < 3; i++) {
            System.out.println(nomes[i]);

        }

    }
}
